# Seperator mit sep=" " setzen, damit werden die Wörter mit einem beliebigen Trennzeichen getrennt
print("Wir","schreiben","unseren","ersten","Code!", sep="-")
print("Wir","schreiben","unseren","ersten","Code!", sep="1")

#Jeder print()-Befehl enthält automatisch am Ende einen Zeilenumbruch.
print("Das Ende eines Print-Befehls enthält automatisch einen Zeilenumbruch.")
print("Ende")

#Leerzeichen oder beliebiges Zeichen anstatt Zeilenumbruch
print("Das Ende eines Print-Befehls enthält automatisch einen Zeilenumbruch.", end=" ")
print("Ende")

print("Das Ende eines Print-Befehls enthält automatisch einen Zeilenumbruch.", end="-")
print("Ende")
